def line_one(a,b):
    # Add
    
    return a+b

def line_two(a,b):
    # Subtract
    
    return a-b

def line_three(a,b):
    # Multiply
    
    return a*b


if __name__ == '__main__':
    a = int(input())
    b = int(input())

    print(line_one(a,b))
    print(line_two(a,b))
    print(line_three(a,b))
        
